/**
 * Normalizes a course name for matching against verified URLs
 * Converts to lowercase, removes special characters, standardizes formatting
 */
export const normalizeCourseName = (name: string): string => {
  return name
    .toLowerCase()
    .replace(/[®™©]/g, '')
    .replace(/[()]/g, '')
    .replace(/[:;,.']/g, '')
    .replace(/&/g, 'and')
    .replace(/\+/g, '')
    .replace(/\s+/g, ' ')
    .trim();
};

/**
 * Creates a normalized lookup map for faster matching
 */
const normalizedLookup: Record<string, string> = {};
Object.entries(verifiedCourseUrls).forEach(([name, url]) => {
  normalizedLookup[normalizeCourseName(name)] = url;
});

/**
 * Gets a verified course URL by course name
 * Returns null if the course URL is not verified
 */
export const getVerifiedCourseUrl = (courseName: string): string | null => {
  // Check exact match first
  if (verifiedCourseUrls[courseName]) {
    return verifiedCourseUrls[courseName];
  }
  
  // Check normalized match
  const normalized = normalizeCourseName(courseName);
  if (normalizedLookup[normalized]) {
    return normalizedLookup[normalized];
  }
  
  // No verified URL found
  return null;
};
